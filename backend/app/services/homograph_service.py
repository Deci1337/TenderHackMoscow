def resolve_homograph(word: str, user_industry: str | None = None) -> list[str]:
    """Return context-appropriate expansions for a homograph word."""
    word_lower = word.lower().strip()
    if word_lower not in HOMOGRAPH_DICT:
        return []

    contexts = HOMOGRAPH_DICT[word_lower]
    if user_industry:
        industry = user_industry.lower().strip()
        industry = INDUSTRY_ALIASES.get(industry, industry)
        if industry in contexts:
            return contexts[industry]

    return contexts.get("default", [])
